package com.ratseno.model;

public class MemberVO {
    //회원 번호
    private int id;
    //회원 명
    private String name;
    //회원 나이
    private int age;

    public MemberVO(int id, String name, int age) {
        this.id = id;
        this.name = name;
        this.age = age;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }
}
